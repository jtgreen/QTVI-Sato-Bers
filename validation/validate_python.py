#!/usr/bin/env python3
"""
validate_python.py — Cross-validate the Python Sato-Bers port against C++ reference traces.

PURPOSE:
    Reads CSV reference traces generated by validate_export (C++ ground truth)
    and reproduces the same scenarios using the Python Cell class. Compares all
    15 state variables at every sample point using float64 tolerances.

VALIDATION SCENARIOS:
    1. Single-beat deterministic trace (equation-level correctness)
    2. Multi-beat convergence (stability across beats)
    3. APD restitution curve (tauf sweep 20–60)
    4. Perturbation tests:
       a. Elevated gk1 conductance (2x, simulating hyperkalemia)
       b. 50% ICaL block (icabar halved)
       c. Enhanced SERCA (2x vup)
       d. Depolarized initial condition (v0 = -60 mV)
    5. Stochastic gating trace (xorshift32 RNG reproducibility)
    6. Steady-state fingerprint (exact match after 500 beats)

TOLERANCES:
    - Deterministic scenarios: rtol=1e-12, atol=1e-12 (bit-level agreement expected)
    - Stochastic scenario: rtol=1e-10, atol=1e-10 (minor float rounding in RNG)
    - APD/CaMax: rtol=1e-8, atol=1e-8 (accumulated integration differences)

OUTPUT:
    Prints PASS/FAIL for each scenario with max absolute/relative error.
    Also writes Python traces to validation/results/ for manual inspection.

USAGE:
    cd validation
    python validate_python.py
"""

import sys
import os
import csv
import math
import numpy as np

# Add parent python/ directory to path so we can import the Cell class
sys.path.insert(0, os.path.join(os.path.dirname(__file__), '..', 'python'))
from cell import Cell, xorshift

# ============================================================================
# Constants: state variable names matching CSV column headers
# ============================================================================
STATE_NAMES = ['v', 'ci', 'cs', 'cj', 'cjp', 'Ir', 'f', 'q', 'd', 'h', 'j',
               'XKr', 'XKs', 'Xto', 'Yto']


def load_reference_csv(filename):
    """
    Load a C++ reference CSV file into a list of dicts.

    Each row becomes a dict with keys: 'time', 'v', 'ci', 'cs', ..., 'Yto'.
    All values are parsed as float64. Raises FileNotFoundError if the
    reference data hasn't been generated yet.

    Parameters
    ----------
    filename : str
        Path to the CSV file relative to the current working directory.

    Returns
    -------
    list of dict
        Each dict maps column names to float64 values.
    """
    rows = []
    with open(filename, 'r') as f:
        reader = csv.DictReader(f)
        for row in reader:
            rows.append({k: float(v) for k, v in row.items()})
    return rows


def get_cell_state_dict(cell):
    """
    Extract the 15 state variables from a Cell object as a dict.

    This provides a convenient way to compare the Python cell state
    against reference CSV rows column-by-column.

    Parameters
    ----------
    cell : Cell
        The Python cell model instance.

    Returns
    -------
    dict
        Maps state variable names to their current float64 values.
    """
    return {
        'v': cell.v, 'ci': cell.ci, 'cs': cell.cs, 'cj': cell.cj, 'cjp': cell.cjp,
        'Ir': cell.Ir, 'f': cell.f, 'q': cell.q, 'd': cell.d,
        'h': cell.h, 'j': cell.j, 'XKr': cell.XKr, 'XKs': cell.XKs,
        'Xto': cell.Xto, 'Yto': cell.Yto
    }


def compare_traces(ref_rows, py_rows, rtol=1e-12, atol=1e-12):
    """
    Compare reference (C++) and Python state traces element-by-element.

    For each (time, variable) pair, computes the absolute difference and
    the relative difference (using max(|ref|, |py|) as denominator).
    Reports the worst-case error across all points and variables.

    Parameters
    ----------
    ref_rows : list of dict
        C++ reference trace from load_reference_csv().
    py_rows : list of dict
        Python trace collected during simulation.
    rtol : float
        Relative tolerance. Comparison passes if |ref-py|/max(|ref|,|py|) < rtol.
    atol : float
        Absolute tolerance. Comparison passes if |ref-py| < atol.

    Returns
    -------
    tuple (bool, float, float, str)
        (passed, max_abs_err, max_rel_err, worst_variable)
    """
    max_abs = 0.0
    max_rel = 0.0
    worst_var = ""

    # Compare row counts — length mismatch is an immediate failure
    if len(ref_rows) != len(py_rows):
        print(f"  WARNING: row count mismatch: ref={len(ref_rows)}, py={len(py_rows)}")
        n = min(len(ref_rows), len(py_rows))
    else:
        n = len(ref_rows)

    for i in range(n):
        for name in STATE_NAMES:
            ref_val = ref_rows[i][name]
            py_val = py_rows[i][name]
            abs_err = abs(ref_val - py_val)
            denom = max(abs(ref_val), abs(py_val), 1e-300)
            rel_err = abs_err / denom

            if abs_err > max_abs:
                max_abs = abs_err
                if abs_err > max_rel:
                    worst_var = f"{name} at row {i} (t={ref_rows[i].get('time', '?')})"
            if rel_err > max_rel:
                max_rel = rel_err

    passed = (max_abs < atol) or (max_rel < rtol)
    return passed, max_abs, max_rel, worst_var


def run_cell_beat(cell, bcl, stim, dt, sample_interval=10):
    """
    Run the cell model for one full beat and collect sampled state traces.

    Applies stimulus during the first STIM_DURATION ms of the beat cycle,
    then free-runs for the remainder. Samples state every `sample_interval` steps.

    Parameters
    ----------
    cell : Cell
        Cell model instance (already pre-paced to desired state).
    bcl : float
        Basic cycle length in ms.
    stim : float
        Stimulus current amplitude.
    dt : float
        Integration time step in ms.
    sample_interval : int
        Record state every this many time steps.

    Returns
    -------
    list of dict
        Sampled state trace with 'time' and all 15 state variable keys.
    """
    BCLn = int(bcl / dt)
    Durn = int(Cell.STIM_DURATION / dt)
    trace = []
    for tn in range(BCLn):
        t = tn * dt
        if tn % sample_interval == 0:
            row = {'time': t}
            row.update(get_cell_state_dict(cell))
            trace.append(row)
        if tn < Durn:
            cell.pace(stim)
        else:
            cell.pace()
    return trace


def prepace(cell, beats, bcl, stim, dt):
    """
    Pre-pace the cell model for a given number of beats.

    Pre-pacing drives the model toward its limit-cycle attractor
    from arbitrary initial conditions, ensuring subsequent measurements
    reflect steady-state behavior rather than transient dynamics.

    Parameters
    ----------
    cell : Cell
        Cell model instance.
    beats : int
        Number of pre-pacing beats.
    bcl : float
        Basic cycle length in ms.
    stim : float
        Stimulus current amplitude.
    dt : float
        Integration time step.
    """
    BCLn = int(bcl / dt)
    Durn = int(Cell.STIM_DURATION / dt)
    for beat in range(beats):
        for tn in range(BCLn):
            if tn < Durn:
                cell.pace(stim)
            else:
                cell.pace()


def save_trace_csv(trace, filename):
    """
    Write a state trace to a CSV file for manual inspection / plotting.

    Parameters
    ----------
    trace : list of dict
        State trace as returned by run_cell_beat().
    filename : str
        Output CSV file path.
    """
    if not trace:
        return
    with open(filename, 'w', newline='') as f:
        writer = csv.DictWriter(f, fieldnames=['time'] + STATE_NAMES)
        writer.writeheader()
        for row in trace:
            writer.writerow({k: f"{row[k]:.17g}" for k in ['time'] + STATE_NAMES})


