def scenario1():
    """
    Scenario 1: Single-beat deterministic trace.

    Reproduces C++ scenario with N_CaL=0, tauf=30, 100-beat prepace,
    then records one beat at 0.5 ms resolution (every 10 steps).
    """
    print("Scenario 1: Single beat deterministic trace")
    cell = Cell()
    cell.N_CaL = 0
    cell.tauf = 30
    cell.av = 3.0
    dt = cell.dt
    stim = Cell.STIM
    bcl = 300.0

    prepace(cell, 100, bcl, stim, dt)
    trace = run_cell_beat(cell, bcl, stim, dt, sample_interval=10)
    save_trace_csv(trace, "validation/results/scenario1_python.csv")

    ref = load_reference_csv("validation/reference/scenario1_single_beat.csv")
    passed, max_abs, max_rel, worst = compare_traces(ref, trace)
    status = "PASS" if passed else "FAIL"
    print(f"  {status}: max_abs_err={max_abs:.3e}, max_rel_err={max_rel:.3e}")
    if not passed:
        print(f"  Worst: {worst}")
    return passed


def scenario2():
    """
    Scenario 2: Multi-beat convergence trace (10 beats after 100-beat prepace).

    Verifies no numerical drift across beats. Samples every 20 steps = 1.0 ms.
    """
    print("Scenario 2: Multi-beat convergence trace")
    cell = Cell()
    cell.N_CaL = 0
    cell.tauf = 30
    cell.av = 3.0
    dt = cell.dt
    stim = Cell.STIM
    bcl = 300.0

    prepace(cell, 100, bcl, stim, dt)

    # Record 10 beats
    BCLn = int(bcl / dt)
    Durn = int(Cell.STIM_DURATION / dt)
    total_steps = 10 * BCLn
    trace = []
    for tn in range(total_steps):
        t = tn * dt
        if tn % 20 == 0:
            row = {'time': t}
            row.update(get_cell_state_dict(cell))
            trace.append(row)
        if tn % BCLn < Durn:
            cell.pace(stim)
        else:
            cell.pace()

    save_trace_csv(trace, "validation/results/scenario2_python.csv")
    ref = load_reference_csv("validation/reference/scenario2_multi_beat.csv")
    passed, max_abs, max_rel, worst = compare_traces(ref, trace)
    status = "PASS" if passed else "FAIL"
    print(f"  {status}: max_abs_err={max_abs:.3e}, max_rel_err={max_rel:.3e}")
    if not passed:
        print(f"  Worst: {worst}")
    return passed


def scenario3():
    """
    Scenario 3: APD restitution — tauf sweep from 20 to 60.

    Compares APD and CaMax values per beat across the entire tauf range.
    Uses 200-beat prepace + 100-beat measurement per tauf (matching C++ reference).
    """
    print("Scenario 3: APD restitution (tauf sweep 20-60)")
    cell = Cell()
    cell.N_CaL = 0
    cell.av = 3.0
    dt = cell.dt
    stim = Cell.STIM
    vc = Cell.VC

    py_results = []
    for tf in range(20, 61):
        cell.tauf = float(tf)
        bcl = 300.0
        BCLn = int(bcl / dt)
        Durn = int(Cell.STIM_DURATION / dt)

        # Pre-pace 200 beats
        prepace_Tn = 200 * BCLn
        for tn in range(prepace_Tn):
            if tn % BCLn < Durn:
                cell.pace(stim)
            else:
                cell.pace()

        # Measure 100 beats
        measure_Tn = 100 * BCLn
        first = False
        cimax = 0.0
        apd = 0.0
        vold = cell.v
        APDt1 = 0.0
        for tn in range(measure_Tn):
            t = tn * dt
            if tn % BCLn < Durn:
                if first:
                    first = False
                    py_results.append({'tauf': tf, 'apd': apd, 'cimax': cimax})
                    cimax = 0.0
                    apd = 0.0
                cell.pace(stim)
            else:
                first = True
                cell.pace()
            if cimax < cell.ci:
                cimax = cell.ci
            if vold < vc and cell.v > vc:
                APDt1 = (t - dt) + dt * (vc - vold) / (cell.v - vold)
            elif vold > vc and cell.v < vc:
                APDt2 = (t - dt) + dt * (vc - vold) / (cell.v - vold)
                apd = APDt2 - APDt1
            vold = cell.v

    # Compare against reference
    ref = load_reference_csv("validation/reference/scenario3_apd_restitution.csv")

    # Compare APD and cimax values
    n = min(len(ref), len(py_results))
    max_apd_err = 0.0
    max_ci_err = 0.0
    for i in range(n):
        apd_err = abs(ref[i]['apd'] - py_results[i]['apd'])
        ci_err = abs(ref[i]['cimax'] - py_results[i]['cimax'])
        max_apd_err = max(max_apd_err, apd_err)
        max_ci_err = max(max_ci_err, ci_err)

    passed = max_apd_err < 1e-8 and max_ci_err < 1e-8
    status = "PASS" if passed else "FAIL"
    print(f"  {status}: max_apd_err={max_apd_err:.3e}, max_cimax_err={max_ci_err:.3e}")
    print(f"  Compared {n} beat measurements across tauf={20}-{60}")
    return passed


def scenario4_helper(name, ref_file, configure_fn):
    """
    Generic helper for perturbation tests.

    Creates a cell, applies the configure_fn to set perturbation parameters,
    pre-paces for 100 beats, records 1 beat, and compares against reference.

    Parameters
    ----------
    name : str
        Human-readable scenario name for display.
    ref_file : str
        Path to the C++ reference CSV.
    configure_fn : callable
        Function that takes a Cell and modifies it (e.g., cell.gk1 = 5.6).

    Returns
    -------
    bool
        True if scenario passed, False otherwise.
    """
    cell = Cell()
    cell.N_CaL = 0
    cell.tauf = 30
    cell.av = 3.0
    configure_fn(cell)
    dt = cell.dt
    stim = Cell.STIM
    bcl = 300.0

    prepace(cell, 100, bcl, stim, dt)
    trace = run_cell_beat(cell, bcl, stim, dt, sample_interval=10)

    ref = load_reference_csv(ref_file)
    passed, max_abs, max_rel, worst = compare_traces(ref, trace, rtol=1e-11, atol=1e-11)
    status = "PASS" if passed else "FAIL"
    print(f"  {name}: {status} (max_abs={max_abs:.3e}, max_rel={max_rel:.3e})")
    if not passed:
        print(f"    Worst: {worst}")
    return passed


def scenario4():
    """
    Scenario 4: Perturbation tests — verify identical response to parameter changes.

    4a: Doubled gk1 (simulates hyperkalemia effect on IK1)
    4b: 50% ICaL block (icabar = 1.75)
    4c: Enhanced SERCA (vup = 0.500)
    4d: Depolarized start (v = -60 mV)
    """
    print("Scenario 4: Perturbation tests")
    results = []
    results.append(scenario4_helper("4a: 2x gk1",
        "validation/reference/scenario4a_elevated_gk1.csv",
        lambda c: setattr(c, 'gk1', 5.6)))
    results.append(scenario4_helper("4b: 50% ICaL block",
        "validation/reference/scenario4b_ical_block.csv",
        lambda c: setattr(c, 'icabar', 1.75)))
    results.append(scenario4_helper("4c: 2x SERCA",
        "validation/reference/scenario4c_enhanced_serca.csv",
        lambda c: setattr(c, 'vup', 0.500)))
    results.append(scenario4_helper("4d: v0=-60mV",
        "validation/reference/scenario4d_depolarized.csv",
        lambda c: setattr(c, 'v', -60.0)))
    return all(results)


def scenario5():
    """
    Scenario 5: Stochastic gating trace — xorshift32 RNG reproducibility.

    The stochastic gating uses a Langevin equation with noise from
    Box-Muller transform of xorshift32 uniform variates. The Python
    xorshift implementation must produce identical bit patterns as C++.

    Uses slightly relaxed tolerance (1e-10) since stochastic mode
    may amplify small float64 rounding differences over 50 pre-pace beats.
    """
    print("Scenario 5: Stochastic gating (fixed seed)")
    cell = Cell()
    cell.N_CaL = 100000
    cell.tauf = 30
    cell.av = 3.0
    cell.xsx = 1821800813
    dt = cell.dt
    stim = Cell.STIM
    bcl = 300.0

    prepace(cell, 50, bcl, stim, dt)
    trace = run_cell_beat(cell, bcl, stim, dt, sample_interval=10)
    save_trace_csv(trace, "validation/results/scenario5_python.csv")

    ref = load_reference_csv("validation/reference/scenario5_stochastic.csv")
    passed, max_abs, max_rel, worst = compare_traces(ref, trace, rtol=1e-10, atol=1e-10)
    status = "PASS" if passed else "FAIL"
    print(f"  {status}: max_abs_err={max_abs:.3e}, max_rel_err={max_rel:.3e}")
    if not passed:
        print(f"  Worst: {worst}")
    return passed


def scenario6():
    """
    Scenario 6: Steady-state fingerprint — exact match after 500 beats.

    After 500 beats of pre-pacing, the model should be deeply converged
    to its limit cycle. The final state is a single-point "fingerprint"
    that must match the C++ reference to near machine precision.
    """
    print("Scenario 6: Steady-state fingerprint (500 beats)")
    cell = Cell()
    cell.N_CaL = 0
    cell.tauf = 30
    cell.av = 3.0
    dt = cell.dt
    stim = Cell.STIM
