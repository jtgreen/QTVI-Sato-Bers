#!/usr/bin/env julia
#=
validate_julia.jl — Cross-validate the Julia Sato-Bers port against C++ reference traces.

PURPOSE:
    Reads CSV reference traces generated by validate_export (C++ ground truth)
    and reproduces the same scenarios using the Julia SatoBers module. Compares
    all 15 state variables at every sample point using float64 tolerances.

VALIDATION SCENARIOS:
    1. Single-beat deterministic trace (equation-level correctness)
    2. Multi-beat convergence (stability across beats)
    3. APD restitution curve (tauf sweep 20–60)
    4. Perturbation tests:
       a. Elevated gk1 conductance (2x, simulating hyperkalemia)
       b. 50% ICaL block (icabar halved)
       c. Enhanced SERCA (2x vup)
       d. Depolarized initial condition (v0 = -60 mV)
    5. Stochastic gating trace (xorshift32 RNG reproducibility)
    6. Steady-state fingerprint (exact match after 500 beats)

TOLERANCES:
    Same as Python validation:
    - Deterministic: rtol=1e-12, atol=1e-12
    - Stochastic: rtol=1e-10, atol=1e-10
    - APD/CaMax: rtol=1e-8, atol=1e-8

USAGE:
    cd <project_root>
    julia validation/validate_julia.jl
=#

# Load the SatoBers module from the julia/ directory
include(joinpath(@__DIR__, "..", "julia", "SatoBers.jl"))
using .SatoBers
using DelimitedFiles

# ============================================================================
# State variable names matching CSV column headers (indices 1–15)
# ============================================================================
const STATE_NAMES = ["v", "ci", "cs", "cj", "cjp", "Ir", "f", "q", "d",
                     "h", "j", "XKr", "XKs", "Xto", "Yto"]

"""
    load_reference_csv(filename) -> (header, data)

Load a C++ reference CSV file. Returns column headers and a Matrix{Float64}
where each row is a time point and columns are [time, v, ci, ..., Yto].
"""
function load_reference_csv(filename::String)
    lines = readlines(filename)
    header = split(lines[1], ',')
    n = length(lines) - 1
    ncol = length(header)
    data = zeros(Float64, n, ncol)
    for i in 1:n
        vals = split(lines[i+1], ',')
        for j in 1:ncol
            data[i, j] = parse(Float64, vals[j])
        end
    end
    return header, data
end

"""
    compare_traces(ref_data, jl_data; rtol=1e-12, atol=1e-12)

Compare reference and Julia state traces element-by-element.
ref_data and jl_data are matrices with columns [time, v, ci, ..., Yto].

Returns (passed, max_abs_err, max_rel_err, worst_info).
"""
function compare_traces(ref_data::Matrix{Float64}, jl_data::Matrix{Float64};
                        rtol=1e-12, atol=1e-12)
    n = min(size(ref_data, 1), size(jl_data, 1))
    if size(ref_data, 1) != size(jl_data, 1)
        println("  WARNING: row count mismatch: ref=$(size(ref_data,1)), jl=$(size(jl_data,1))")
    end

    max_abs = 0.0
    max_rel = 0.0
    worst = ""

    # Compare columns 2–16 (state variables, skip time column 1)
    for i in 1:n
        for j in 2:min(size(ref_data,2), size(jl_data,2))
            ref_val = ref_data[i, j]
            jl_val  = jl_data[i, j]
            abs_err = abs(ref_val - jl_val)
            denom   = max(abs(ref_val), abs(jl_val), 1e-300)
            rel_err = abs_err / denom

            if abs_err > max_abs
                max_abs = abs_err
                if j-1 <= length(STATE_NAMES)
                    worst = "$(STATE_NAMES[j-1]) at row $i (t=$(ref_data[i,1]))"
                end
            end
            max_rel = max(max_rel, rel_err)
        end
    end

    passed = (max_abs < atol) || (max_rel < rtol)
    return passed, max_abs, max_rel, worst
end

"""
    prepace!(u, du, model, beats, bcl, stim, dt; rng=nothing)

Pre-pace the cell model for `beats` beats using Forward Euler.
If `rng` is provided (a StochasticState), stochastic gating is applied.
"""
function prepace!(u, du, model, beats, bcl, stim, dt; rng=nothing)
    BCLn = round(Int, bcl / dt)
    Durn = round(Int, 1.0 / dt)  # 1 ms stimulus duration
    for beat in 1:beats
        for tn in 0:BCLn-1
            if tn < Durn
                cell_rhs_deterministic!(du, u, model, stim)
            else
                cell_rhs_deterministic!(du, u, model, 0.0)
            end
            if rng !== nothing
                stochastic_gate_update!(u, du, dt, model, rng)
            end
            @. u += du * dt
        end
    end
end

"""
    run_beat!(u, du, model, bcl, stim, dt, sample_interval; rng=nothing)

Run one beat and return a matrix of sampled state traces.
Columns: [time, v, ci, cs, ..., Yto] (16 columns total).
"""
function run_beat!(u, du, model, bcl, stim, dt, sample_interval; rng=nothing)
    BCLn = round(Int, bcl / dt)
    Durn = round(Int, 1.0 / dt)
    nsamples = div(BCLn, sample_interval)
    trace = zeros(Float64, nsamples, 16)
    idx = 0
    for tn in 0:BCLn-1
        t = tn * dt
        if mod(tn, sample_interval) == 0
            idx += 1
            if idx <= nsamples
                trace[idx, 1] = t
                trace[idx, 2:16] .= u[1:15]
            end
        end
        if tn < Durn
            cell_rhs_deterministic!(du, u, model, stim)
        else
            cell_rhs_deterministic!(du, u, model, 0.0)
        end
        if rng !== nothing
            stochastic_gate_update!(u, du, dt, model, rng)
        end
        @. u += du * dt
    end
    return trace[1:idx, :]
end

