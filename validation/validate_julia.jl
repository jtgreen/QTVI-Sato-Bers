# ============================================================================
# Main — run all scenarios
# ============================================================================
function main()
    println("=" ^ 60)
    println("Sato-Bers Model: Julia Validation Suite")
    println("=" ^ 60)
    println()

    results = Dict{String, Bool}()
    results["scenario1"] = scenario1(); println()
    results["scenario2"] = scenario2(); println()
    results["scenario3"] = scenario3(); println()
    results["scenario4"] = scenario4(); println()
    results["scenario5"] = scenario5(); println()
    results["scenario6"] = scenario6()

    println()
    println("=" ^ 60)
    println("SUMMARY")
    println("=" ^ 60)
    all_passed = true
    for (name, passed) in sort(collect(results))
        status = passed ? "PASS" : "FAIL"
        println("  $name: $status")
        if !passed
            all_passed = false
        end
    end
    println()
    if all_passed
        println("ALL SCENARIOS PASSED")
    else
        println("SOME SCENARIOS FAILED — see details above")
    end
    return all_passed ? 0 : 1
end

exit(main())
