"""
    scenario1() -> Bool

Scenario 1: Single-beat deterministic trace.
N_CaL=0, tauf=30, 100-beat prepace, 1 beat recorded at 0.5 ms resolution.
"""
function scenario1()
    println("Scenario 1: Single beat deterministic trace")
    model = SatoBersModel{Float64}(tauf=30.0, av=3.0, N_CaL=0)
    dt = 0.05; stim = 50.0; bcl = 300.0
    u  = collect(default_initial_state(model))
    du = zeros(15)

    prepace!(u, du, model, 100, bcl, stim, dt)
    trace = run_beat!(u, du, model, bcl, stim, dt, 10)

    _, ref = load_reference_csv("validation/reference/scenario1_single_beat.csv")
    passed, max_abs, max_rel, worst = compare_traces(ref, trace)
    status = passed ? "PASS" : "FAIL"
    println("  $status: max_abs_err=$(round(max_abs, sigdigits=3)), max_rel_err=$(round(max_rel, sigdigits=3))")
    !passed && println("  Worst: $worst")
    return passed
end

"""
    scenario2() -> Bool

Scenario 2: Multi-beat convergence trace (10 beats, sampled every 20 steps).
"""
function scenario2()
    println("Scenario 2: Multi-beat convergence trace")
    model = SatoBersModel{Float64}(tauf=30.0, av=3.0, N_CaL=0)
    dt = 0.05; stim = 50.0; bcl = 300.0
    u  = collect(default_initial_state(model))
    du = zeros(15)

    prepace!(u, du, model, 100, bcl, stim, dt)

    # Record 10 beats
    BCLn = round(Int, bcl / dt)
    Durn = round(Int, 1.0 / dt)
    total = 10 * BCLn
    nsamples = div(total, 20)
    trace = zeros(Float64, nsamples, 16)
    idx = 0
    for tn in 0:total-1
        t = tn * dt
        if mod(tn, 20) == 0
            idx += 1
            if idx <= nsamples
                trace[idx, 1] = t
                trace[idx, 2:16] .= u[1:15]
            end
        end
        if mod(tn, BCLn) < Durn
            cell_rhs_deterministic!(du, u, model, stim)
        else
            cell_rhs_deterministic!(du, u, model, 0.0)
        end
        @. u += du * dt
    end
    trace = trace[1:idx, :]

    _, ref = load_reference_csv("validation/reference/scenario2_multi_beat.csv")
    passed, max_abs, max_rel, worst = compare_traces(ref, trace)
    status = passed ? "PASS" : "FAIL"
    println("  $status: max_abs_err=$(round(max_abs, sigdigits=3)), max_rel_err=$(round(max_rel, sigdigits=3))")
    !passed && println("  Worst: $worst")
    return passed
end

"""
    scenario3() -> Bool

Scenario 3: APD restitution — tauf sweep 20–60, 200-beat prepace + 100-beat measurement.
Compares APD and peak Ca²⁺ per beat.
"""
function scenario3()
    println("Scenario 3: APD restitution (tauf sweep 20-60)")
    dt = 0.05; stim = 50.0; vc = -70.0
    u  = zeros(15)
    du = zeros(15)

    # Initialize state to defaults
    u .= collect(default_initial_state(SatoBersModel{Float64}()))

    jl_results = Vector{NTuple{3,Float64}}()

    for tf in 20:60
        model = SatoBersModel{Float64}(tauf=Float64(tf), av=3.0, N_CaL=0)
        bcl = 300.0
        BCLn = round(Int, bcl / dt)
        Durn = round(Int, 1.0 / dt)

        # Pre-pace 200 beats
        prepace!(u, du, model, 200, bcl, stim, dt)

        # Measure 100 beats
        Tn = 100 * BCLn
        first = false
        cimax = 0.0; apd = 0.0; vold = u[1]; APDt1 = 0.0

        for tn in 0:Tn-1
            t = tn * dt
            if mod(tn, BCLn) < Durn
                if first
                    first = false
                    push!(jl_results, (Float64(tf), apd, cimax))
                    cimax = 0.0; apd = 0.0
                end
                cell_rhs_deterministic!(du, u, model, stim)
            else
                first = true
                cell_rhs_deterministic!(du, u, model, 0.0)
            end
            @. u += du * dt
            if cimax < u[2]
                cimax = u[2]
            end
            if vold < vc && u[1] > vc
                APDt1 = (t - dt) + dt * (vc - vold) / (u[1] - vold)
            elseif vold > vc && u[1] < vc
                APDt2 = (t - dt) + dt * (vc - vold) / (u[1] - vold)
                apd = APDt2 - APDt1
            end
            vold = u[1]
        end
    end

    # Compare
    _, ref = load_reference_csv("validation/reference/scenario3_apd_restitution.csv")
    n = min(size(ref, 1), length(jl_results))
    max_apd_err = 0.0; max_ci_err = 0.0
    for i in 1:n
        apd_err = abs(ref[i, 2] - jl_results[i][2])
        ci_err  = abs(ref[i, 3] - jl_results[i][3])
        max_apd_err = max(max_apd_err, apd_err)
        max_ci_err  = max(max_ci_err,  ci_err)
    end

    passed = max_apd_err < 1e-8 && max_ci_err < 1e-8
    status = passed ? "PASS" : "FAIL"
    println("  $status: max_apd_err=$(round(max_apd_err, sigdigits=3)), max_cimax_err=$(round(max_ci_err, sigdigits=3))")
    println("  Compared $n beat measurements across tauf=20-60")
    return passed
end

"""
    scenario4_helper(name, ref_file, model) -> Bool

Helper for perturbation tests. Pre-paces 100 beats, records 1 beat, compares.
"""
function scenario4_helper(name, ref_file, model)
    dt = 0.05; stim = 50.0; bcl = 300.0
    u  = collect(default_initial_state(model))
    du = zeros(15)

    prepace!(u, du, model, 100, bcl, stim, dt)
    trace = run_beat!(u, du, model, bcl, stim, dt, 10)

    _, ref = load_reference_csv(ref_file)
    passed, max_abs, max_rel, worst = compare_traces(ref, trace, rtol=1e-11, atol=1e-11)
    status = passed ? "PASS" : "FAIL"
    println("  $name: $status (max_abs=$(round(max_abs, sigdigits=3)), max_rel=$(round(max_rel, sigdigits=3)))")
    !passed && println("    Worst: $worst")
    return passed
end

"""
    scenario4() -> Bool

Scenario 4: Perturbation tests.
4a: 2x gk1, 4b: 50% ICaL, 4c: 2x SERCA, 4d: depolarized start.
"""
function scenario4()
    println("Scenario 4: Perturbation tests")
    r = Bool[]
    push!(r, scenario4_helper("4a: 2x gk1",
        "validation/reference/scenario4a_elevated_gk1.csv",
        SatoBersModel{Float64}(tauf=30.0, av=3.0, N_CaL=0, gk1=5.6)))
    push!(r, scenario4_helper("4b: 50% ICaL block",
        "validation/reference/scenario4b_ical_block.csv",
        SatoBersModel{Float64}(tauf=30.0, av=3.0, N_CaL=0, icabar=1.75)))
    push!(r, scenario4_helper("4c: 2x SERCA",
        "validation/reference/scenario4c_enhanced_serca.csv",
        SatoBersModel{Float64}(tauf=30.0, av=3.0, N_CaL=0, vup=0.500)))

    # 4d: Depolarized start — modify initial state
    model_d = SatoBersModel{Float64}(tauf=30.0, av=3.0, N_CaL=0)
    dt = 0.05; stim = 50.0; bcl = 300.0
    u  = collect(default_initial_state(model_d))
    u[1] = -60.0  # Depolarized initial condition
    du = zeros(15)
    prepace!(u, du, model_d, 100, bcl, stim, dt)
    trace = run_beat!(u, du, model_d, bcl, stim, dt, 10)
    _, ref = load_reference_csv("validation/reference/scenario4d_depolarized.csv")
    passed, max_abs, max_rel, worst = compare_traces(ref, trace, rtol=1e-11, atol=1e-11)
    status = passed ? "PASS" : "FAIL"
    println("  4d: v0=-60mV: $status (max_abs=$(round(max_abs, sigdigits=3)), max_rel=$(round(max_rel, sigdigits=3)))")
    !passed && println("    Worst: $worst")
    push!(r, passed)

    return all(r)
end

"""
    scenario5() -> Bool

Scenario 5: Stochastic gating trace with fixed xorshift32 seed.
Tests RNG reproducibility across C++ and Julia.
"""
function scenario5()
    println("Scenario 5: Stochastic gating (fixed seed)")
    model = SatoBersModel{Float64}(tauf=30.0, av=3.0, N_CaL=100000)
    dt = 0.05; stim = 50.0; bcl = 300.0
