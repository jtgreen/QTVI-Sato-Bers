    _, ref = load_reference_csv("validation/reference/scenario5_stochastic.csv")
    passed, max_abs, max_rel, worst = compare_traces(ref, trace, rtol=1e-10, atol=1e-10)
    status = passed ? "PASS" : "FAIL"
    println("  $status: max_abs_err=$(round(max_abs, sigdigits=3)), max_rel_err=$(round(max_rel, sigdigits=3))")
