# Figure 2B — APD vs beat number (alternans pattern)
# =============================================================================
def fig_apd_per_beat():
    """
    Reproduce Fig 2B from Sato et al. 2025.

    Shows APD vs beat number for several tauf values, illustrating:
    - Periodic (constant) APD at low tauf (stable)
    - Beat-to-beat alternation (2:1) at high tauf (alternans)
    - Stochastic variability visible at intermediate tauf values
    """
    data = load_csv('apd_per_beat.csv')
    tauf_vals = sorted(set(int(r['tauf']) for r in data))
    by_tauf = defaultdict(list)
    for r in data:
        by_tauf[int(r['tauf'])].append(r)

    n = len(tauf_vals)
    nrows = math.ceil(n / 2)
    fig, axes = plt.subplots(nrows, 2, figsize=(10, 3 * nrows), sharey=False)
    if n == 1:
        axes = [[axes]]
    axes = [ax for row in axes for ax in row]

    for i, tf in enumerate(tauf_vals):
        ax = axes[i]
        rows = by_tauf[tf]
        beats = [r['beat'] for r in rows]
        apds  = [r['apd']  for r in rows]
        camx  = [r['camax'] for r in rows]

        ax.plot(beats, apds, 'b-', linewidth=0.8, alpha=0.8)
        ax.set_title(f'τ_f = {tf} ms')
        ax.set_ylabel('APD (ms)')
        ax.set_xlabel('Beat #')

        # Compute and display variability
        arr = np.array(apds)
        std = np.std(arr)
        ax.text(0.97, 0.97, f'std={std:.2f} ms', transform=ax.transAxes,
                ha='right', va='top', fontsize=8, color='navy')

    # Hide unused subplots
    for j in range(len(tauf_vals), len(axes)):
        axes[j].set_visible(False)

    fig.suptitle('APD vs Beat Number at Different τ_f Values\n(u = 3.0, N_CaL = 100,000)',
                 fontsize=11)
    plt.tight_layout()
    outfile = os.path.join(OUT_DIR, 'fig5_apd_per_beat.png')
    fig.savefig(outfile, bbox_inches='tight')
    plt.close(fig)
    print(f'Saved: {outfile}')


