# Stability boundary (Fig 1) — requires combined tauf + u data
# Build from tauf and u sweep data
# =============================================================================
def fig_stability_boundary():
    """
    Reproduce Fig 1 from Sato et al. 2025 — the stability boundary map.

    The stability boundary separates:
    - Stable region (periodic APs, no alternans)
    - Vm-driven alternans (high tauf, varied u)
    - Ca2+-driven alternans (high u, varied tauf)

    Derived from:
    - tauf_sweep: onset of alternans as a function of tauf (u fixed at 3.0)
    - u_sweep_iclamp: onset of Ca2+ alternans as a function of u (tauf fixed at 35)
    """
    tauf_data = load_csv('tauf_sweep.csv')
    u_data    = load_csv('u_sweep_iclamp.csv')

    # Determine alternans threshold: APD spread > 5 ms
    tauf_alt  = None
    for r in tauf_data:
        if r['apd_max'] - r['apd_min'] > 5.0:
            tauf_alt = r['tauf']
            break

    u_alt = None
    for r in u_data:
        if r['apd_max'] - r['apd_min'] > 5.0:
            u_alt = r['u']
            break

    # Build a rough stability map
    # Points in (tauf, u) space:
    # - From tauf sweep (u=3): mark stable vs alternans
    # - From u sweep (tauf=35): mark stable vs alternans
    tauf_vals  = [r['tauf']  for r in tauf_data]
    spread_t   = [r['apd_max'] - r['apd_min'] for r in tauf_data]

    u_vals     = [r['u']     for r in u_data]
    spread_u   = [r['apd_max'] - r['apd_min'] for r in u_data]
    ca_spread_u = [r['ca_max'] - r['ca_min'] for r in u_data]

    fig, axes = plt.subplots(1, 2, figsize=(12, 5))

    # Left: APD spread vs tauf (at u=3)
    ax = axes[0]
    ax.fill_between(tauf_vals, 0, spread_t, alpha=0.3, color='steelblue',
                    label='APD spread (alternans amplitude)')
    ax.axhline(5, color='red', linestyle='--', linewidth=1, label='Alternans threshold (5 ms)')
    if tauf_alt:
        ax.axvline(tauf_alt, color='royalblue', linestyle=':', linewidth=1.5,
                   label=f'Onset: τ_f = {tauf_alt} ms')
    ax.set_xlabel('τ_f (ms; LTCC recovery)')
    ax.set_ylabel('APD_max - APD_min (ms)')
    ax.set_title('Voltage-Driven Instability\n(u = 3.0, tauf sweep)')
    ax.legend(loc='upper left', fontsize=8)
    ax.set_ylim(bottom=0)

    # Right: APD spread vs u (tauf=35)
    ax = axes[1]
    ax.fill_between(u_vals, 0, spread_u, alpha=0.3, color='darkorange',
                    label='APD spread')
    ax.fill_between(u_vals, 0, ca_spread_u, alpha=0.2, color='firebrick',
                    label='Ca²⁺ spread')
    ax.axhline(5, color='red', linestyle='--', linewidth=1, label='APD alternans threshold')
    if u_alt:
        ax.axvline(u_alt, color='darkorange', linestyle=':', linewidth=1.5,
                   label=f'APD alternans onset: u = {u_alt}')
    ax.set_xlabel('u (SR Ca²⁺ gain, ms⁻¹)')
    ax.set_ylabel('Spread (ms or µM)')
    ax.set_title('Ca²⁺-Driven Instability\n(τ_f = 35 ms, u sweep)')
    ax.legend(loc='upper left', fontsize=8)
    ax.set_ylim(bottom=0)

    plt.tight_layout()
    outfile = os.path.join(OUT_DIR, 'fig1_stability_boundary.png')
    fig.savefig(outfile, bbox_inches='tight')
    plt.close(fig)
    print(f'Saved: {outfile}')


