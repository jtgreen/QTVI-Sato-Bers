# Figure 2 / 4A — APD bifurcation diagram + variability vs tauf
# =============================================================================
def fig_tauf_sweep():
    """
    Reproduce Fig 2A and Fig 4A (top + bottom) from Sato et al. 2025.

    Panel A (top): APD bifurcation diagram — scatter of APD min and max vs tauf.
      Shows period-doubling (alternans) above a critical tauf value.
    Panel B (middle): APD variability (std) vs tauf — every beat (black) and
      every-other-beat (red). Shows variability peak at onset of alternans.
    Panel C (bottom): APD mean vs tauf — shows lengthening then shortening
      as alternans progresses.
    """
    data = load_csv('tauf_sweep.csv')
    tauf    = [r['tauf']         for r in data]
    apd_mu  = [r['apd_mean']     for r in data]
    apd_sig = [r['apd_std']      for r in data]
    apd_min = [r['apd_min']      for r in data]
    apd_max = [r['apd_max']      for r in data]
    apd_alt = [r['apd_std_altbeat'] for r in data]
    ca_mu   = [r['ca_mean']      for r in data]
    ca_sig  = [r['ca_std']       for r in data]
    ca_min  = [r['ca_min']       for r in data]
    ca_max  = [r['ca_max']       for r in data]

    fig, axes = plt.subplots(3, 1, figsize=(7, 8), sharex=True)

    # --- Panel A: Bifurcation diagram ---
    ax = axes[0]
    ax.scatter(tauf, apd_min, s=8, c='steelblue', alpha=0.7, label='APD min/max')
    ax.scatter(tauf, apd_max, s=8, c='steelblue', alpha=0.7)
    ax.set_ylabel('APD (ms)')
    ax.set_title('APD Bifurcation Diagram (tauf sweep, u = 3.0)')
    ax.legend([Line2D([0],[0],marker='o',color='w',markerfacecolor='steelblue',
                      markersize=5)], ['APD scatter (min/max)'], loc='upper left')

    # Mark onset of alternans (where APD_max - APD_min > 5 ms)
    for i, (lo, hi, tf) in enumerate(zip(apd_min, apd_max, tauf)):
        if hi - lo > 5.0:
            ax.axvline(tf, color='royalblue', linestyle='--', alpha=0.5, linewidth=1)
            ax.annotate('alternans\nonset', xy=(tf, max(apd_max)*0.7),
                        fontsize=8, color='royalblue', ha='center')
            break

    # --- Panel B: APD variability ---
    ax = axes[1]
    ax.plot(tauf, apd_sig, 'k-o', markersize=3, label='Every beat (std)')
    ax.plot(tauf, apd_alt, 'r--s', markersize=3, label='Every other beat (std)')
    ax.set_ylabel('APD variability (ms)')
    ax.set_title('APD Variability vs τ_f')
    ax.legend(loc='upper left')

    # --- Panel C: Ca2+ peak ---
    ax = axes[2]
    ax.scatter(tauf, ca_min, s=8, c='firebrick', alpha=0.7)
    ax.scatter(tauf, ca_max, s=8, c='firebrick', alpha=0.7)
    ax.set_ylabel('[Ca²⁺]_i peak (µM)')
    ax.set_xlabel('τ_f (ms; LTCC recovery)')
    ax.set_title('[Ca²⁺]_i Peak Bifurcation')

    plt.tight_layout()
    outfile = os.path.join(OUT_DIR, 'fig2_tauf_sweep.png')
    fig.savefig(outfile, bbox_inches='tight')
    plt.close(fig)
    print(f'Saved: {outfile}')


