# Figure 3B — Ca2+ transient per beat (AP-clamp, different u values)
# =============================================================================
def fig_ca_per_beat():
    """
    Reproduce Fig 3B from Sato et al. 2025.

    Shows Ca2+ transient peak vs beat number for different u values under
    AP-clamp. Demonstrates Ca2+ alternans onset as u increases, independent
    of AP variability (since voltage is clamped to a fixed waveform).
    """
    data = load_csv('ca_per_beat_apclamp.csv')
    u_vals = sorted(set(r['u'] for r in data))
    by_u = defaultdict(list)
    for r in data:
        by_u[r['u']].append(r)

    n = len(u_vals)
    fig, axes = plt.subplots(2, 2, figsize=(10, 6))
    axes = [ax for row in axes for ax in row]

    colors = ['steelblue', 'darkorange', 'firebrick', 'purple']
    for i, u_val in enumerate(u_vals):
        ax = axes[i]
        rows = by_u[u_val]
        beats = [r['beat'] for r in rows]
        ca    = [r['camax'] for r in rows]

        ax.plot(beats, ca, color=colors[i % len(colors)], linewidth=0.8)
        ax.set_title(f'u = {u_val} ms⁻¹ (AP-clamp, τ_f = 35 ms)')
        ax.set_ylabel('[Ca²⁺]_i peak (µM)')
        ax.set_xlabel('Beat #')

        arr = np.array(ca)
        std = np.std(arr)
        ax.text(0.97, 0.97, f'std={std:.4f} µM', transform=ax.transAxes,
                ha='right', va='top', fontsize=8, color='black')

    fig.suptitle('Ca²⁺ Transient Peak vs Beat (AP-clamp)\n'
                 'Increasing u drives Ca²⁺ cycling instability', fontsize=11)
    plt.tight_layout()
    outfile = os.path.join(OUT_DIR, 'fig6_ca_per_beat.png')
    fig.savefig(outfile, bbox_inches='tight')
    plt.close(fig)
    print(f'Saved: {outfile}')


