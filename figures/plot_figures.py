# Figure 3 / 4B/4C — u sweep (I-clamp and AP-clamp)
# =============================================================================
def fig_u_sweep():
    """
    Reproduce Figs 3 and 4B/4C from Sato et al. 2025.

    Shows how increasing u (SR Ca2+ release steepness) drives:
    - Ca2+ transient alternans in AP-clamp mode (Fig 4B)
    - APD alternans and variability in current-clamp mode (Fig 4C)
    """
    iclamp = load_csv('u_sweep_iclamp.csv')
    apclamp = load_csv('u_sweep_apclamp.csv')

    u_ic   = [r['u']               for r in iclamp]
    apd_ic = [r['apd_mean']        for r in iclamp]
    sig_ic = [r['apd_std']         for r in iclamp]
    alt_ic = [r['apd_std_altbeat'] for r in iclamp]
    ca_ic  = [r['ca_mean']         for r in iclamp]
    lo_ic  = [r['apd_min']         for r in iclamp]
    hi_ic  = [r['apd_max']         for r in iclamp]

    u_ap   = [r['u']               for r in apclamp]
    ca_ap  = [r['ca_mean']         for r in apclamp]
    sig_ap = [r['ca_std']          for r in apclamp]
    alt_ap = [r['ca_std_altbeat']  for r in apclamp]
    lo_ap  = [r['ca_min']          for r in apclamp]
    hi_ap  = [r['ca_max']          for r in apclamp]

    fig, axes = plt.subplots(2, 2, figsize=(12, 8))
    fig.suptitle('u sweep: Ca²⁺-driven Instability (τ_f = 35 ms)', fontsize=12)

    # Top-left: Ca2+ bifurcation (AP-clamp) — Fig 4B top
    ax = axes[0, 0]
    ax.scatter(u_ap, lo_ap, s=8, c='firebrick', alpha=0.7)
    ax.scatter(u_ap, hi_ap, s=8, c='firebrick', alpha=0.7)
    ax.set_title('[Ca²⁺]_i peak (AP-clamp)')
    ax.set_ylabel('[Ca²⁺]_i peak (µM)')
    ax.set_xlabel('u (SR Ca²⁺ gain)')
    for i, (lo, hi, u) in enumerate(zip(lo_ap, hi_ap, u_ap)):
        if hi - lo > 0.05:
            ax.axvline(u, color='firebrick', linestyle='--', alpha=0.5, linewidth=1)
            ax.annotate('Ca²⁺\nalternans', xy=(u, max(hi_ap)*0.55),
                        fontsize=8, color='firebrick', ha='center')
            break

    # Top-right: Ca2+ variability (AP-clamp) — Fig 3A / 4B bottom
    ax = axes[0, 1]
    ax.plot(u_ap, sig_ap, 'k-o', markersize=3, label='Every beat (std)')
    ax.plot(u_ap, alt_ap, 'r--s', markersize=3, label='Every other beat (std)')
    ax.set_title('[Ca²⁺]_i Variability (AP-clamp)')
    ax.set_ylabel('[Ca²⁺]_i variability (µM)')
    ax.set_xlabel('u (SR Ca²⁺ gain)')
    ax.legend(loc='upper left')

    # Bottom-left: APD bifurcation (I-clamp) — Fig 4C top
    ax = axes[1, 0]
    ax.scatter(u_ic, lo_ic, s=8, c='steelblue', alpha=0.7)
    ax.scatter(u_ic, hi_ic, s=8, c='steelblue', alpha=0.7)
    ax.set_title('APD (I-clamp)')
    ax.set_ylabel('APD (ms)')
    ax.set_xlabel('u (SR Ca²⁺ gain)')
    for i, (lo, hi, u) in enumerate(zip(lo_ic, hi_ic, u_ic)):
        if hi - lo > 5.0:
            ax.axvline(u, color='steelblue', linestyle='--', alpha=0.5, linewidth=1)
            ax.annotate('APD\nalternans', xy=(u, max(hi_ic)*0.6),
                        fontsize=8, color='steelblue', ha='center')
            break

    # Bottom-right: APD variability (I-clamp) — Fig 3C / 4C bottom
    ax = axes[1, 1]
    ax.plot(u_ic, sig_ic, 'k-o', markersize=3, label='Every beat (std)')
    ax.plot(u_ic, alt_ic, 'r--s', markersize=3, label='Every other beat (std)')
    ax.set_title('APD Variability (I-clamp)')
    ax.set_ylabel('APD variability (ms)')
    ax.set_xlabel('u (SR Ca²⁺ gain)')
    ax.legend(loc='upper left')

    plt.tight_layout()
    outfile = os.path.join(OUT_DIR, 'fig3_u_sweep.png')
    fig.savefig(outfile, bbox_inches='tight')
    plt.close(fig)
    print(f'Saved: {outfile}')


