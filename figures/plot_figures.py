"""
plot_figures.py — Reproduce key figures from Sato et al. (2025) QTVI paper.

Reads CSV data generated by figures/generate_figure_data.jl and produces:
  - fig1_stability_boundary.png  : Stability map (tauf vs u)
  - fig2_tauf_sweep.png          : APD variability + bifurcation vs tauf (Fig 2/4A)
  - fig3_u_sweep.png             : Ca2+ variability + bifurcation vs u (Fig 3/4B/4C)
  - fig4_ap_traces.png           : Action potential traces at representative tauf values
  - fig5_apd_per_beat.png        : APD vs beat number for selected tauf (Fig 2B)
  - fig6_ca_per_beat.png         : Ca2+ transient peak vs beat (Fig 3B)

All figures use a clean publication-style layout matching the paper.

References:
    Sato D et al. J Physiol (2025).
    DOI: 10.1113/JP289051
"""

import csv
import os
import sys
import math
from collections import defaultdict

import numpy as np
import matplotlib
matplotlib.use('Agg')  # Non-interactive backend for headless generation
import matplotlib.pyplot as plt
