    ca_min_ap = [r['ca_min']  for r in u_apc_data]
    ca_max_ap = [r['ca_max']  for r in u_apc_data]

    # Beat traces for tauf=52, 57 (near bifurcation)
    by_tauf = defaultdict(list)
    for r in beat_data:
        by_tauf[int(r['tauf'])].append(r)

