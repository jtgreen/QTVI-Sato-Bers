    ax02.set_ylabel('APD (ms)')
    ax02.set_xlabel('Beat #')
    ax02.set_title('APD vs Beat # (≈ Fig 2B)')
    ax02.legend(fontsize=8)

    # ---- Row 1 ----
    # (1,0): APD bifurcation vs u (I-clamp)
