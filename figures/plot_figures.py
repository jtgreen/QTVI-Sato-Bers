    ax02 = fig.add_subplot(gs[0, 2])
    colors_t = ['steelblue', 'firebrick']
    for color, tf in zip(colors_t, [52, 57]):
        if tf in by_tauf:
            rows = by_tauf[tf]
            beats = [r['beat'] for r in rows]
