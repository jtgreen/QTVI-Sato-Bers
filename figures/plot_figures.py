# Figure: Action Potential + Ca2+ Transient Traces
# =============================================================================
def fig_ap_traces():
    """
    Show action potential and Ca2+ transient at different tauf values.
    Illustrates how increasing tauf prolongs the AP and modifies Ca2+ cycling.
    """
    data = load_csv('ap_traces.csv')
    tauf_vals = sorted(set(int(r['tauf']) for r in data))
    by_tauf = defaultdict(list)
    for r in data:
        by_tauf[int(r['tauf'])].append(r)

    colors = plt.cm.viridis(np.linspace(0, 0.9, len(tauf_vals)))

    fig, axes = plt.subplots(3, 1, figsize=(9, 8), sharex=True)

    for color, tf in zip(colors, tauf_vals):
        rows = by_tauf[tf]
        t  = np.array([r['time'] for r in rows])
        v  = np.array([r['v']    for r in rows])
        ci = np.array([r['ci']   for r in rows])
        d  = np.array([r['d']    for r in rows])
        f  = np.array([r['f']    for r in rows])

        axes[0].plot(t, v, color=color, linewidth=1.2, label=f'τ_f={tf}')
        axes[1].plot(t, ci, color=color, linewidth=1.2)
        axes[2].plot(t, d * f, color=color, linewidth=1.2)  # ICaL open probability ~ d*f

    axes[0].set_ylabel('V_m (mV)')
    axes[0].set_title('Action Potential')
    axes[0].legend(loc='upper right', ncol=3, fontsize=8)
    axes[0].axhline(-70, color='grey', linestyle=':', linewidth=0.8, alpha=0.7)

    axes[1].set_ylabel('[Ca²⁺]_i (µM)')
    axes[1].set_title('Ca²⁺ Transient')

    axes[2].set_ylabel('d·f (open prob.)')
    axes[2].set_xlabel('Time (ms)')
    axes[2].set_title('ICaL Open Probability (d·f)')

    plt.tight_layout()
    outfile = os.path.join(OUT_DIR, 'fig4_ap_traces.png')
    fig.savefig(outfile, bbox_inches='tight')
    plt.close(fig)
    print(f'Saved: {outfile}')


